package application;

public class Boolean {

	public static void main(String[] args) {
		// 불린 타입은 참 또는 거짓
		boolean c1 = false;		
		System.out.println("조건1: " + c1);
		//비교
		boolean c2 = 4 < 5;
		System.out.println("조건2: " + c2);
		//같다(==) 같지않다.(!=)
		boolean c3 = 4 != 5;
		System.out.println("조건3: " + c3);

	}

}
